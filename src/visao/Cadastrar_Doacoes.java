    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
       Doador da = new Doador();
       da.setCpf(Long.parseLong(txtCPF.getText()));
       opDoador = new OperacaoDoador();
       Date data=conv(txtData.getText());
       java.sql.Date data1;
       data1 = new java.sql.Date(data.getTime());
       try {
            opDoador = new OperacaoDoador();
            da = opDoador.find(da);
                    Doacao dados = null;        
                    dados = new Doacao(
                    Long.parseLong(txtCPF.getText()),
                    Float.parseFloat(txtValor.getText()),
                    data1
                    );
        
        OperacaoDoacao opDoacao = new OperacaoDoacao();
        
        opDoacao.realiza(dados);
        opDoador.atualizaStatus(dados, (java.sql.Date) dados.getData());
        JOptionPane.showMessageDialog(null, "Doação cadastrada com sucesso!");
        limpaCampo();
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "CPF inexistente no banco de dados!");
        } 
    }//GEN-LAST:event_btCadastrarActionPerformed

