    private Date conv(String data) {
        SimpleDateFormat formatData = new SimpleDateFormat("yyyy/MM/dd");
        SimpleDateFormat exib=new SimpleDateFormat("dd/MM/yyyy");
        Date nova=null;
        try {
            nova =exib.parse(data);
            
        } catch (ParseException ex) {
            Logger.getLogger(Lista_DoacoesE.class.getName()).log(Level.SEVERE, null, ex);
        }
        String formatoFinal = formatData.format(nova);
        Date dataf=null;
        System.out.println(formatoFinal);
        try {
            dataf=formatData.parse(formatoFinal);
        } catch (ParseException ex) {
            Logger.getLogger(Cadastrar_Doacoes.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println(dataf);
        return dataf;
    }

