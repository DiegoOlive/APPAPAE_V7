    private Date conv(String data) {
        SimpleDateFormat exib=new SimpleDateFormat("dd/MM/yyyy");
        Date nova=null;
        try {
            nova =exib.parse(data);           
        } catch (ParseException ex) {
            Logger.getLogger(null,"Data inválida");
        }
 
        return nova;
    }
