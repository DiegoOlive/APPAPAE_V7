                String datat = tabela_Gasto.getValueAt(linhaSelecionada, 3).toString();
                String tipo=txtDescricao.getText();
                s=conv(datat);
                Date prazoII = conv(txtData.getText());
                java.sql.Date datan;
                datan = new java.sql.Date(s.getTime());
                java.sql.Date data1;
                data1 = new java.sql.Date(prazoII.getTime());
                long cpf = 0;
                Gasto dados = null;
                dados = new Gasto(
                    tipo,
                    Float.parseFloat(txtValor.getText()),
                    data1,
                    id
                );
                System.out.println("Indo pra doação");
                opGastos = new OperacaoGastos();
