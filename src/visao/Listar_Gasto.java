    private void atualizarGastosTotal(){
        OperacaoDoacao dd = new OperacaoDoacao();
        OperacaoGastos dd1 = new OperacaoGastos();
        
        float rd = dd.somaDoacoes();
        String sd = Float.toString(rd);
        txtTotalDoacoes.setText(sd);
        
        float rg = dd1.somaGastos();
        String sg = Float.toString(rg);
        txtTotalGastos.setText(sg);
        
        float rs = dd.somaDoacoes() - dd1.somaGastos();
        String ss = Float.toString(rs);
        txtTotalTUDO.setText(ss);
    }
