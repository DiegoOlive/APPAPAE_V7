    private String convIn(String data) {
        SimpleDateFormat form = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat form2 = new SimpleDateFormat("dd/MM/yyyy");
        Date s=null;
        try {
                    s=form.parse(data);
        } catch (ParseException ex) {
                    Logger.getLogger(Lista_DoacoesE.class.getName()).log(Level.SEVERE, null, ex);
        }
        String retorno=form2.format(s);
        return retorno;
    }

