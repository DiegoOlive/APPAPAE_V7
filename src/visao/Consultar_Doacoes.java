    private void btn_consultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultaActionPerformed
         Lista_DoacoesE lD = new Lista_DoacoesE();
        DefaultTableModel doacaoModel = (DefaultTableModel) lD.tabela_Doacoes.getModel();
        
        OperacaoDoacao opDoacao = new OperacaoDoacao();
        for (int i = doacaoModel.getRowCount()-1; i >= 0; i--) {
            doacaoModel.removeRow(i);
        }
        long cpf=Long.parseLong(txtCPF.getText());
        Doacao dados = new Doacao();
        try {
            dados.setCpf(Long.parseLong(txtCPF.getText()));
            
            dados = opDoacao.find(dados);
            for (Doacao doacao : opDoacao.listDoacoes(null,cpf)) {
            String[] myList = {
                new String(String.valueOf(doacao.getCpf())),
                new String(String.valueOf(doacao.getValor())),
                new String(String.valueOf(convIn(String.valueOf(doacao.getData())))),
                new String(String.valueOf(String.valueOf(doacao.getId())))
            };
            doacaoModel.addRow(myList);
        }
        lD.permissao(per);
        lD.setVisible(true);
        this.dispose();  
          
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "CPF inexistente no banco de dados!");
        }                                           

    }//GEN-LAST:event_btn_consultaActionPerformed

