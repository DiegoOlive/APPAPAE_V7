    private void btn_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_entrarActionPerformed
       
        long cpft= Long.parseLong(this.txt_cpf.getText());
        String valor = new String(txt_senha.getPassword());
        
        if (this.autenticar.autenticar(cpft, valor)) {
            this.usuarioCorrente = this.autenticar.getUsuario();
            md = new Tela_Modulos();
            //System.out.println("OIII");
            if (this.usuarioCorrente.getTipo().equals("Administrador")) {
                md.permissao(true);
                md.setVisible(true);
                
                this.dispose();
                JOptionPane.showMessageDialog(null, "Seja bem vindo, " + this.usuarioCorrente.getNome());
            } else if (this.usuarioCorrente.getTipo().equals("Usuario")) {
                
                md.permissao(false);
                md.setVisible(true);
                
                this.dispose();
                JOptionPane.showMessageDialog(null, "Seja bem vindo, " + this.usuarioCorrente.getNome());
            } 

        } else {
            JOptionPane.showMessageDialog(null, "Credenciais inv√°lidas");
        }
    }//GEN-LAST:event_btn_entrarActionPerformed

